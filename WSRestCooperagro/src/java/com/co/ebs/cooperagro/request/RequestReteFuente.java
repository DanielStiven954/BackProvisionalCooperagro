package com.co.ebs.cooperagro.request;

public class RequestReteFuente {
    
    private String psIdentificacion;
    private String psFechaIni;
    private String psFechaFin;
    private String sTipoIdentificacion;

    public String getPsIdentificacion() {
        return psIdentificacion;
    }

    public void setPsIdentificacion(String psIdentificacion) {
        this.psIdentificacion = psIdentificacion;
    }

    public String getPsFechaIni() {
        return psFechaIni;
    }

    public void setPsFechaIni(String psFechaIni) {
        this.psFechaIni = psFechaIni;
    }

    public String getPsFechaFin() {
        return psFechaFin;
    }

    public void setPsFechaFin(String psFechaFin) {
        this.psFechaFin = psFechaFin;
    }

    public String getsTipoIdentificacion() {
        return sTipoIdentificacion;
    }

    public void setsTipoIdentificacion(String sTipoIdentificacion) {
        this.sTipoIdentificacion = sTipoIdentificacion;
    }
    
    
}
